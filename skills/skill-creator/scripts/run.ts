function renderRunStub(name: string): string {
  return `#!/usr/bin/env bun
/**
 * ${name}/scripts/run.ts
 *
 * TODO: implement this skill's actual logic. This is a placeholder
 * generated by skill-creator.
 */

import { parseArgs } from "util";

function main() {
  const { values } = parseArgs({
    args: process.argv.slice(2),
    options: {
      help: { type: "boolean", default: false },
    },
    strict: false,
  });

  if (values.help) {
    console.log("${name}: not yet implemented. See SKILL.md for the intended interface.");
    return;
  }

  console.error(JSON.stringify({ error: "not-implemented", message: "${name} has not been implemented yet. Edit scripts/run.ts." }, null, 2));
  process.exit(1);
}

main();
`;
}

function parseFrontmatter(content: string): Record<string, string> | null {
  const m = content.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return null;
  const fields: Record<string, string> = {};
  for (const line of m[1].split("\n")) {
    const fm = line.match(/^(\w+):\s*"?([^"]*)"?\s*$/);
    if (fm) fields[fm[1]] = fm[2];
  }
  return fields;
}

function main() {
  const args = parseCliArgs();
  const skillPath = join(args.skillsDir, args.name);

  if (existsSync(skillPath) && !args.force) {
    console.error(JSON.stringify({ error: "skill-exists", message: `${skillPath} already exists. Use --force to overwrite.` }, null, 2));
    process.exit(1);
  }

  const dirs = ["scripts", "references", "config", "examples", "tests"];
  for (const d of dirs) {
    mkdirSync(join(skillPath, d), { recursive: true });
  }

  const filesCreated: string[] = [];

  const manifestPath = join(skillPath, "SKILL.md");
  writeFileSync(manifestPath, renderManifest(args), "utf-8");
  filesCreated.push(manifestPath);

  const runPath = join(skillPath, "scripts", "run.ts");
  writeFileSync(runPath, renderRunStub(args.name), "utf-8");
  filesCreated.push(runPath);

  const configPath = join(skillPath, "config", "defaults.yaml");
  writeFileSync(configPath, "# TODO: add configuration defaults\n", "utf-8");
  filesCreated.push(configPath);

  const refPath = join(skillPath, "references", "notes.md");
  writeFileSync(refPath, `# Reference Notes\n\nTODO: add reference material for ${args.name}.\n`, "utf-8");
  filesCreated.push(refPath);

  const inputPath = join(skillPath, "examples", "input.md");
  writeFileSync(inputPath, "TODO: add an example CLI invocation\n", "utf-8");
  filesCreated.push(inputPath);

  const outputPath = join(skillPath, "examples", "expected-output.md");
  writeFileSync(outputPath, "TODO: add the expected output for the example above\n", "utf-8");
  filesCreated.push(outputPath);

  const testPath = join(skillPath, "tests", "test_skill.sh");
  writeFileSync(testPath, `#!/usr/bin/env bash\n# TODO: write real tests for ${args.name}\nset -euo pipefail\necho "TODO: not yet implemented"\nexit 1\n`, "utf-8");
  filesCreated.push(testPath);

  // Validate the manifest we just wrote.
  const written = readFileSync(manifestPath, "utf-8");
  const fields = parseFrontmatter(written);
  const requiredFields = ["name", "version", "description", "author", "category", "entrypoint"];
  const manifestValid = !!fields && requiredFields.every((f) => fields[f] && fields[f].length > 0);

  console.log(JSON.stringify({ skillName: args.name, path: skillPath, filesCreated, manifestValid }, null, 2));
  if (!manifestValid) process.exit(1);
}

main();
