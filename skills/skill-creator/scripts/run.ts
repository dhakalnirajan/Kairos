#!/usr/bin/env bun
/**
 * skill-creator/scripts/run.ts
 *
 * Scaffolds a new skill's standard folder structure and SKILL.md manifest.
 * Validates name and category before writing; validates the generated
 * manifest after writing. Does not register or execute anything.
 */

import { parseArgs } from "util";
import { existsSync, mkdirSync, writeFileSync, readFileSync } from "fs";
import { join } from "path";

const VALID_CATEGORIES = ["analysis", "automation", "code-generation", "deployment", "testing", "documentation"];
const NAME_RE = /^[a-z][a-z0-9-]*$/;

interface CreatorArgs {
  name: string;
  description: string;
  category: string;
  author: string;
  skillsDir: string;
  force: boolean;
}

function parseCliArgs(): CreatorArgs {
  const { values } = parseArgs({
    args: process.argv.slice(2),
    options: {
      name: { type: "string" },
      description: { type: "string" },
      category: { type: "string" },
      author: { type: "string", default: "harness-core" },
      "skills-dir": { type: "string", default: ".." },
      force: { type: "boolean", default: false },
    },
  });

  if (!values.name) {
    console.error("Error: --name is required");
    process.exit(1);
  }
  if (!NAME_RE.test(values.name as string)) {
    console.error(`Error: --name must match ${NAME_RE} (lowercase, hyphen-separated)`);
    process.exit(1);
  }
  if (!values.description) {
    console.error("Error: --description is required");
    process.exit(1);
  }
  if (!values.category || !VALID_CATEGORIES.includes(values.category as string)) {
    console.error(`Error: --category must be one of: ${VALID_CATEGORIES.join(", ")}`);
    process.exit(1);
  }

  return {
    name: values.name as string,
    description: values.description as string,
    category: values.category as string,
    author: (values.author as string) ?? "harness-core",
    skillsDir: (values["skills-dir"] as string) ?? "..",
    force: values.force as boolean,
  };
}

